package org.onap.policy.gui.server.config;

import static org.springframework.web.reactive.function.server.RequestPredicates.GET;
import static org.springframework.web.reactive.function.server.RouterFunctions.route;
import static org.springframework.web.reactive.function.server.ServerResponse.ok;

import org.springframework.beans.factory.annotation.Value;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.core.io.Resource;
import org.springframework.http.MediaType;
import org.springframework.web.reactive.function.server.RouterFunction;
import org.springframework.web.reactive.function.server.ServerResponse;

@Configuration
public class StaticContentConfig {

    /**
     * Adds a route for /clamp/.
     * @param html Location of static resource (index.html) for clamp
     * @return route
     */
    @Bean
    public RouterFunction<ServerResponse> clampHtmlRouter(@Value("classpath:/static/clamp/index.html") Resource html) {
        return route(GET("/clamp/"), request -> ok().contentType(MediaType.TEXT_HTML).bodyValue(html));
    }

}
